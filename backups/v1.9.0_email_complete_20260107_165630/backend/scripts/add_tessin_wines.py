    print("=" * 60)
    print("üç∑ TOP 50 TESSINER WEINE - Import")
    print("   Il cuore del Ticino in forma liquida!")
    print("=" * 60)
    
    added = 0
    skipped = 0
    
    for wine in TESSIN_WINES:
        # Pr√ºfen ob Wein bereits existiert
        existing = await db.public_wines.find_one({
            "name": wine["name"],
            "winery": wine["winery"]
        })
        
        if existing:
            print(f"  ‚è≠Ô∏è  {wine['name']} ({wine['winery']}) - gi√† presente")
            skipped += 1
            continue
        
        # Wein-Dokument erstellen
